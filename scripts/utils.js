function doAvg(day, avrg) {
  for (let i = 7; i < day.length; i++) {
    avrg.push(
      Math.round(
        (
          day[i] +
          day[i - 1] +
          day[i - 2] +
          day[i - 3] +
          day[i - 4] +
          day[i - 5] +
          day[i - 6]
        ) / 7
      )
    )
  }
  return avrg
}

